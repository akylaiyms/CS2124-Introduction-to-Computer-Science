import Foundation

var x = 1
while x <= 10 {
    print (x)
    x += 1
}

var y = 10
while y > 0 {
    print(y)
    y -= 1
}

for number in (1...10) {
    print (number)
}

for number in (1...10).reversed() {
    print (number)
}
